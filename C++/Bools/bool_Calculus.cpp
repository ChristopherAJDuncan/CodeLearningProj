using namespace std;
#include<iostream>


int main(){

  bool t = true;
  bool f = false;

  cout << "T+F: " << t+f << endl;
  cout << "T+T: " << t+t << endl;
  cout << "F+F: " << f+f << endl;

  cout << endl;

  cout << "T*F: " << t*f << endl;
  cout << "T*T: " << t*t << endl;
  cout << "F*F: " << f*f << endl;


}
